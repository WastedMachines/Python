#Análisis de datos
#Ejemplo 1
#Encontrar el número más grande usando max()
print ("Encontrar el número más grande \n")
number1 = int(input("Ingresa el primer número: "))
number2 = int(input("Ingresa el segundo número: "))
number3 = int(input("Ingresa el tercer número: "))
largest_number = max(number1, number2, number3)
print ("El número más grande es:", largest_number, "\n")

#Encontrar el número más pequeño usando min()
print ("Encontrar el número más pequeño \n")
number1 = int(input("Ingresa el primer número: "))
number2 = int(input("Ingresa el segundo número: "))
number3 = int(input("Ingresa el tercer número: "))
miniest_number = min(number1, number2, number3)
print ("El número más pequeño es:", miniest_number, "\n")

